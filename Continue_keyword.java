package assignments;

public class Continue_keyword {

	public static void main(String[] args) {
		for(int i=0;i<=10;i++)//write for loop to print 1 to 10
		{
			if(i==7) 
			{
				continue;// continue statement can be used to skip this particular i==7 iteration
			
			}
			System.out.println(i);
		}
		

	}

}
